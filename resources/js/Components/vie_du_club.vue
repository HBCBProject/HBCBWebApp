          <h2>
            Le réglement intérieur<br />
          </h2>
          <p>
            <br/>(en vigueur au 3 juin 2018)
            <br/>AVANT-PROPOS
            <br/>L’adhésion à l’association HBCB est un engagement individuel pour chaque licencié du club, mais
            également un engagement à l’association où chacun (joueur, parent, dirigeant, administrateur, bénévole …)
            doit participer dans la mesure de ses disponibilités.
            <br/>Afin d’organiser la vie associative du club, il est défini dans le présent règlement intérieur les
            droits et devoirs de chacun.
            <br/>I. CONSTITUTION ET ADOPTION DU PRESENT REGLEMENT INTERIEUR
            <br/>Ce règlement intérieur a pour but de préciser et de compléter les articles des statuts de l’association.
            <br/>Son contenu et son adoption sont soumis au vote du Conseil d’Administration. Il sera diffusé auprès de
            l’ensemble des adhérents après modification. Chaque adhérent s’engage tacitement à le lire et le respecter.
            <br/>II. SIÈGE SOCIAL
            <br/>Le siège social de l’association est fixé à l’adresse suivante :
            <br/>HERIC HANDBALL
            <br/>Mairie de Blain
            <br/>12 rue de l’Océan
            <br/>44130 Blain
            <br/>III. BUREAU ET CONSEIL D'AMINISTRATION
            <br/>Article 1 Bureau et Conseil d’Administration
            <br/>Le Conseil d’administration élu lors de l’Assemblée Générale procède à l’élection du Bureau directeur
            <br/>lors de la première séance suivante. Les modalités de vote sont définies par le Conseil d’Administration
            en début de séance.
            <br/>La prise de fonction du Bureau devient effective dès la clôture des scrutins.
            <br/>Le Conseil d’Administration met en place des commissions. Celles-ci sont obligatoirement dirigées par un
            membre du Conseil d’Administration mais peuvent être composées de membres actifs volontaires.
            <br/>REGLEMENT INTERIEUR DE L’ASSOCIATION HBCB
            <br/>Article 2 Commissions
            <br/>Le principe de fonctionnement de chaque commission est l'autonomie, dans le respect des dispositions
            légales et conventionnelles du club ainsi que de la maîtrise des dépenses.
            <br/>Toute commission ne respectant pas ces dispositions et/ou portant atteinte aux intérêts du club et à la
            réalisation de son projet sera dissoute et des sanctions pourront être prises à l'encontre des membres la
            composant.
            <br/>Article 3 Séances du Conseil d’Administration
            <br/>Le CA se réunit au moins 2 fois par an conformément aux statuts du club.
            <br/>Sur invitation de CA, les salariés et/ou bénévoles de l'association peuvent assister aux séances de ce
            dernier, avec voix consultatives. Peuvent également être invitées les personnes dont les compétences sont
            utiles à la prise de décision du Conseil d'Administration.
            <br/>IV. LES MEMBRES DE L’ASSOCIATION
            <br/>Article 1 Adhésion
            <br/>Conformément aux dispositions statutaires, pour être membre de l’association il faut être agréé par le
            Conseil d’Administration.
            <br/>L’agrément vaut pour l’ensemble de la saison sportive.
            <br/>Le refus d’agrément par le Conseil d’Administration n’a pas à être motivé.
            <br/>Article 2 Cotisations
            <br/>• La cotisation du dirigeant non pratiquant est gratuite.
            <br/>• Un joueur dirigeant paie une part forfaitaire de la cotisation.
            <br/>• Pour les joueurs, différentes grilles de cotisation existent pour une même année de naissance :
            <br/>- cotisations pour renouvellement ou nouvelle adhésion dont le dossier complet est remis avant la date
            butoir de l'année en cours, fixée par le Conseil d'Administration (généralement mi-juillet)
            <br/>- cotisations faisant suite à un renouvellement après cette date.
            <br/>Des facilités de paiement pourront être accordées par le Conseil d'Administration.
            <br/>Les aides financières (Pass'sport, chèques ANCV...) seront déduites du montant de la cotisation - sous
            réserve d'affiliation à l'organisme concerné - si les coupons sont présentés lors de l'inscription.
            <br/>Dans le cas contraire, les licenciés régleront la totalité du montant de la cotisation. Le club
            remboursera aux intéressé(e)s la somme concernée dès réception du justificatif par le secrétariat.
            <br/>Mutations :
            <br/>Sauf accord contraire, tout joueur arrivant au club devra s'acquitter des frais de mutation en plus de
            sa cotisation. Le Conseil d'Administration validera le remboursement de ces frais selon les modalités
            suivantes :
            <br/>- remboursement de la première moitié à la signature de la deuxième année de licence
            <br/>- remboursement de la seconde moitié à la signature de la troisième année.
            <br/>Article 3
            <br/>Les membres de l’association et les membres du Conseil d’Administration sont tenus à l’obligation de
            discrétion au sujet des dossiers ou des faits dont ils ont connaissance lors des réunions ou des
            conversations échangées dans le cadre de leur adhésion ou mandat.
            <br/>Article 4 Sanctions et perte de la qualité de membre
            <br/>Le Conseil d'Administration pourra prononcer des sanctions à l'encontre d'un membre de l'association
            pour motif grave :
            <br/>• manquement à l'éthique sportive
            <br/>• menace et/ou voie de fait dans le cadre des activités du club
            <br/>• activité illégale au sein de l'association
            <br/>• tout comportement déterminé comme néfaste à l'activité de l'association.
            <br/>Les sanctions suivront la chronologie suivante :
            <br/>• convocation par lettre ou courriel, de l'intéressé(e) par le Président pour un entretien avec le
            Conseil d'Administration
            <br/>• avertissement ou suspension prononcé par le Conseil d'Administration
            <br/>• En cas d'absence de la personne convoquée, le Conseil d'Administration délibérera et l'avisera de la
            décision prise par lettre ou courriel.
            <br/>• Si le problème persiste ou s'aggrave : convocation à un nouvel entretien par courriel. Audition des
            explications puis délibérations du Conseil d'Administration sur la prononciation ou non de l'exclusion de
            l'intéressé
            <br/>• En cas d'exclusion, l'intéressé(e) est averti(e) de la décision par courriel ou lettre remise en main
            propre ou lettre recommandée. L'exclusion devient définitive et l'intéressé(e) sera considéré(e) comme
            informé(e) même s'il ou elle ne se manifeste pas.
            <br/>• L'exclusion est la sanction ultime faisant suite à l'ensemble des procédures de sanctions. Dans ce
            cas, l'intéressé(e) doit impérativement et sans délai restituer le matériel du club éventuellement en sa
            possession. Il ou elle ne pourra en aucun cas exiger tout ou partie des cotisations versées pour l'année en
            cours.
            <br/>V. LE JOUEUR ET SES REPRÉSENTANTS LÉGAUX
            <br/>Article 1
            <br/>Tout joueur doit être licencié au club. Il doit fournir notamment un certificat médical ainsi que tout
            autre document demandé dans le dossier d’inscription.
            <br/>Article 2
            <br/>Le joueur ne pourra s’entraîner que lorsqu’il aura déposé son dossier complet au club et que les
            instances fédérales l’auront validé.
            <br/>Article 3
            <br/>Le matériel et les locaux (salles, vestiaires …) sont indispensables à la pratique du handball. Chacun
            s’engage à les respecter et à ne pas les détériorer.
            <br/>Article 4
            <br/>La présence à tous les entraînements et aux matchs est obligatoire. Cependant, en cas d’absence, les
            joueurs doivent avertir le(s) coach(s) et/ou le(s) entraîneur(s) afin qu’il(s) puisse(nt) en tenir compte
            pour l’organisation des entraînements et des matchs.
            <br/>Article 5
            <br/>Les parents ou responsables légaux doivent accompagner l'enfant et s'assurer de la présence des
            encadrants.
            <br/>Article 6
            <br/>Au cours des matchs comme lors des entraînements, il est exigé de chacun le respect envers ses
            partenaires, le(s) entraîneur(s), le(s) dirigeant(s) d’équipe, les adversaires, les arbitres et le public.
            <br/>Chacun doit être conscient qu’un comportement irresponsable peut pénaliser l’équipe et le club.
            <br/>Article 7
            <br/>Tout licencié sanctionné par les instances disciplinaires de la FFHB ou de ses délégations locales
            (Ligue et/ou Comité) devra acquitter l’éventuelle amende accompagnant la sanction sportive.
            <br/>Article 8
            <br/>Un licencié convoqué par les instances disciplinaires sera reçu par les membres du Bureau, ou toute
            autre instance désignée à cet effet, qui, selon le cas, pourra également le sanctionner.
            <br/>Article 9
            <br/>Tout joueur ou parent s'engage à ne pas diffuser d'images prises dans le cadre des activités de
            l'association, à des fins de diffamation, de discrimination ou pécuniaires, sous peine de sanctions
            disciplinaires.
            <br/>Article 10
            <br/>Le joueur doit appliquer les consignes de jeu données par l’entraîneur qui est le seul habilité à les
            définir.
            <br/>Article 11
            <br/>Tout joueur désireux de s’absenter ou de démissionner du club en cours d’année doit en informer
            l’entraîneur et le Bureau. Il ne pourra prétendre à aucun remboursement.
            <br/>Article 12
            <br/>Tout joueur blessé, ne participant pas aux activités du club, ne pourra prétendre à aucun remboursement
            de licence.
            <br/>Article 13
            <br/>Tout joueur blessé, à l’entraînement ou en match, doit impérativement prévenir le jour même le
            responsable d’équipe et/ou le secrétariat du club.
            <br/>Article 14
            <br/>Chaque parent ou représentant légal est sollicité pour effectuer le transport des jeunes pour les matchs
            à l’extérieur, selon un planning établi par le responsable d’équipe.
            <br/>En cas d’indisponibilité, il doit s’arranger avec une autre famille, en permutant les tours, et en
            informer le responsable d’équipe.
            <br/>Toute personne (parents, joueurs, entraîneurs ou autres) transportant des joueurs ou d'autres membres de
            l'association dans son véhicule personnel doit vérifier, auprès de son assurance, la validité de son contrat
            pour ce type de transport. Tout conducteur de son propre véhicule ou d'un véhicule mis à sa disposition est
            responsable de ses passagers et s'engage à veiller à leur sécurité pendant le transport et jusqu'au retour
            au rendez-vous convenu. L'association en pourra en aucun cas être considérée comme responsable des dommages
            matériels ou humains pouvant subvenir lors de ces déplacements.
            <br/>Article 15
            <br/>Chaque parent ou représentant légal est sollicité pour le lavage du jeu de maillots. Le sac de maillots
            doit être de retour dans le local de rangement au plus tard le mercredi suivant le match.
            <br/>VI. LE RESPONSABLE D'ÉQUIPE
            <br/>Article 1
            <br/>Le responsable d'équipe doit être licencié au club, Il partage la responsabilité de l'équipe avec
            l'entraîneur, selon leurs domaines de compétences respectifs. Le responsable d'équipe est notamment chargé
            de l'organisation matérielle et de la diffusion de l'information au sein de l'équipe.
            <br/>Il est l'interlocuteur privilégié entre l'équipe, l'entraîneur et les parents.
            <br/>Il se doit de faire respecter le présent règlement.
            <br/>Article 2
            <br/>En début de saison, il établit un planning pour les transports et le lavage des maillots.
            <br/>Article 3
            <br/>En cas de problème avec un ou plusieurs joueurs, il doit en référer en premier lieu au bureau directeur.
            <br/>Article 4
            <br/>En cas d'accident lors d'un match, il doit en informer le secrétariat du club.
            <br/>Article 5
            <br/>Le responsable d'équipe s'assure lors de chaque match du bon renseignement de la feuille de match
            électronique, avant et après le match.
            <br/>Article 6
            <br/>Tout responsable d'équipe sanctionné par les instances disciplinaires de la FFHB ou de ses délégations
            locales (Ligue et/ou Comité) devra acquitter l'éventuelle amende accompagnant la sanction sportive.
            <br/>Article 7
            <br/>Un responsable d'équipe convoqué par les instances disciplinaires sera reçu par les membres du Bureau,
            ou toute autre instance désignée à cet effet, qui selon le cas pourra également le sanctionner.
            <br/>VII. L'ENTRAINEUR
            <br/>Article 1
            <br/>L'entraîneur doit être licencié du club.
            <br/>Son rôle est de mettre en place un projet sportif pour le(s) équipe(s) dont il a la charge et de le
            mener à bien, dans les limites et le respect des objectifs du club définis par la commission technique.
            <br/>Article 2
            <br/>Il est tenu d'assurer ses obligations d'entraîneur.
            <br/>Il est le seul habilité à définir et donner les consignes de jeu. A ce titre, il est libre dans ses
            choix pour la composition des équipes.
            <br/>Article 3
